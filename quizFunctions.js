let hint = document.getElementById('hintPopup');
let fact = document.getElementById('factPopup');

function openHint() {
    hint.classList.add("open-popup");
}

function closeHint() {
    hint.classList.remove("open-popup");
}

function openFact() {
    fact.classList.add("open-popup");
}

function closeFact() {
    fact.classList.remove("open-popup");
}
